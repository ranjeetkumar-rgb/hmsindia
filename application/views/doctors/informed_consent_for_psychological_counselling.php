<p><strong>Psychotherapy </strong></p>
<ul>
<li>Voluntary Participation: All clients voluntarily agree to treatment, and accordingly may terminate any time. Psychological Counseling involves commitment of time, money, and energy, </li>
<li>Client Involvement: All clients are expected to show up to appointments on time, prepared to focus on and discuss therapy goals and issues, and will not attend while under the influence of mood altering chemicals. All clients are expected to be open and honest so your therapist can assist you better. Counseling is not like a medical doctor consultation. Instead, it calls for a very active effort on your part. In order for therapy to be most successful, you are encouraged to work on things we talk about both during your sessions and at home. Inconsistent attendance can negatively affect your therapy progress.</li>
<li>Psychologist / Therapist Involvement: Your Psychologist / therapist will be prepared at the designated time of the session, (barring emergencies), and will be attentive and supportive to help meet the session goals and do everything possible to assist you in achieving a greater sense of self-awareness and work toward helping you get better insights to resolve problems related to emotional strain and challenges </li>
<li>Guarantees: Although the majority of people do get better in therapy, some do get worse. Accordingly, your therapist makes no guarantee of results. It is not possible to guarantee results such as: becoming happier, saving marriages, stopping drug abuse, becoming less depressed, anxious, pregnant, increasing fertility success and so forth. </li>
<li>Risks of Therapy: Just as medications sometimes causes unexpected side effects, counseling can stimulate painful memories, unanticipated changes in your life, and uncomfortable feelings like sadness, guilt, anger, frustration, loneliness, and helplessness. In some cases, client’s symptoms become worse during the course of therapy, occasionally necessitating hospitalization. Another risk of therapy is that throughout the process of therapeutic change it is not uncommon for clients to reach a point of change where they may feel they are different and no longer able to be the same person they were upon entering therapy. At times these feelings can be unsettling.</li>
<li>Benefits of Psychological counselling/therapy: Psychological counselling/therapy may be physical or online, one-one-one or in a group. It can include: solutions to help you cope with emotional challenges, new insights, more effective means of communicating in relationships, symptomatic relief, improved self-esteem and resilience. </li>
<li>Alternative/Addition to Traditional Therapy: which you may need to undertake as directed by the psychologist / therapist and can include: stress management guided practices, guided selfcare, self-directed relaxation techniques, mindfulness based cognitive therapy, and support groups</li>
<li>Counseling Approach & Theory: Your therapist may use different approaches with a Humanistic orientation to counseling be it one-on-one or in a group, physical or online. It may include building a relationship with clients, creating a nurturing environment to make it conducive to change, exploration of past events and how they continue to affect you today, understanding of how certain belief systems may lead to inadequate functioning or hindrance to change, and specific emotional, cognitive, and behavioral techniques designed to aid in meeting goals such as building resilience, self-esteem and cognitive reappraisal. </li>
<li>Colleague Consultation: In keeping with standards of practice, your therapist may consult with other mental health professionals regarding care and management of cases. The purpose of this consultation is to ensure quality of care. Your therapist will maintain complete confidentiality and protect your identity by not using real names or any identifying information.</li>
</ul>
